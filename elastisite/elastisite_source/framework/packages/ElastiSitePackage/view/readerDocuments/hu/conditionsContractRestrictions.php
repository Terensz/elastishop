<b>A felhasználás korlátai</b>
<br><br>
A szerződés nem jön létre olyan esetben, amennyiben a megrendelés az ebben a fejezetben tárgyalt bármelyik korlátozásba ütközik. Amennyiben a szerződéskötést követően derül ki, hogy a Megrendelő nem hozott a Webfejlesztő tudomására bármilyen okot, ami miatt a megrendelés korlátozásba ütközne, úgy a Webfejlesztő felbonthatja a szerződést, amennyiben a webes alkalmazás még nem került átadásra.<br>
Ha ilyen esetben a webes alkalmazás már átadásra került, úgy a webfejlesztő felszólíthatja a Megrendelőt a tartalom eltávolítására.
<br><br>
<b>A felhasználás területi korlátai</b>
<br><br>
- ElastiSite alapú webes alkalmazások nem vehetők igénybe <b>Kína</b> és <b>Észak-Korea</b> területén.<br>
- Az ElastiSite keretrendszer naplóz minden feltörési kisérletet. Amennyiben a látogató feltörési kísérletet tett, majd felszólítás után folytatta a webhely és a webes alkalmazás sérülékenységének vizsgálatát, úgy a szoftver a hacker kitiltásával fogja megakadályozni a jogsértés folytatását.
<br><br>
<b>A megrendelés személyi korlátai</b>
<br><br>
A Webfejlesztő jogot formál arra, hogy a megrendelők között prioritást alakítson ki, vagy a szerződéskötést indoklás nélkül megtagadja. Ez a jog nem terjed ki a már megkötött szerződésekre, amennyiben a megkötött szerződés nem ütközik az ebben a fejezetben szereplő korlátok egyikébe sem. Amennyiben mégis, úgy a Webfejlesztő elállhat a szeződéstől.
