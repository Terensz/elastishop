<b>A webfejlesztés-szolgáltatások elkészültségének feltételei</b>

<br><br>

Ahhoz, hogy a szerződés teljesüljön, az alábbi lépéseknek kell megtörténnie:

<br><br>

- A Webfejlesztő elkészíti szoftvert, ami a Webfejlesztő szerint pontosan a Felhasználói Kézikönyv és - amennyiben készült ilyen dokumentum - a project specifikációjában leírtak szerint működik. A két dokumentum együttesen a továbbiakban Specifikáció-ként fog szerepelni <br>
- A Webfejlesztő feltölti a szoftvert a Megrendelő által megadott tárhelyre (ha a Megrendelő rendelt Beüzemelési tehermentesítés szolgáltatást, akkor ezzel nincs teendője)<br>
- A Webfejlesztő megküldni a Megrendelő részére a szoftver elérését, a Felhasználói kézikönyv elérését és az elkészített szoftverhez tartozó bejelentkezési adatokat<br>
- A Megrendelő gondosan leteszteli a szoftvert, és amennyiben nem talál benne hibát valamint azt állapítja meg, hogy a szoftver működése maradéktalanul megfelel a Specifikációban leírtaknak, úgy kifizeti a szerződésben szereplő összeget. 

<br><br>

<b>A Beüzemelési tehermentesítés szolgáltatás elkészültségének feltételei</b>

<br><br>

- A fenti "A webfejlesztés-szolgáltatások elkészültségének feltételei" bekezdésben foglaltak teljesültek<br>
- A böngésző URL-sávjában szerepel a https, illetve mutatja a titkosított kapcsolatot a böngésző, és nem ad semmilyen tanusítványra vonatkozó hibaüzenetet és figyelmeztetést

