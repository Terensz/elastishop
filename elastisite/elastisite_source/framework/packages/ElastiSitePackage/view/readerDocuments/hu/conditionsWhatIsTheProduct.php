<b>A termék</b>
<br><br>
A Megrendelő és a Webfejlesztő között létrejövő szerződés akkor teljesül, ha a webfejlesztő átadja a terméket, ami a szerződés tárgyát képezi, a Megrendelő pedig a Webfejlesztő rendelkezésére bocsátja a termék szerződésben foglalt ellenértékét.
<br>
Amennyiben az elkészített termék megfelel a termék dokumentációjában leírtaknak, úgy a Megrendelő köteles azt átvenni.
<br><br>
A webfejlesztő többféle terméket szolgáltat, így azok teljesülési kritériumait termékenként kell meghatározni.
<br><br>
<b>Alapcsomag</b>
<br><br>

Az Alapcsomag termék tartalma:<br>
- File-okból és mappákból álló szoftver, aminek működése megfelel a Felhasználói Kézikönyvben foglaltaknak, amennyiben a futtatási környezet megfelel a Felhasználói Kézikönyv Felhasználás Követelményei fejezetében leírtaknak, és a beüzemelése a Beüzemelési Kézikönyvben leírt lépések szerint történt<br>
- Felhasználói Kézikönyv: a Felhasználás Követelményeinek listája és a szoftver pontos működési dokumentációja<br> 
- Beüzemelési Kézikönyv: a szoftver elindításához szükséges lépések dokumentációja

<br><br>
<b>Webfejlesztés</b>
<br><br>

A Webfejlesztés termék tartalma:<br>
- Az Alapcsomag termék, lásd "Az Alapcsomag termék tartalma"<br>
- Az Alapcsomagon felül a szerződéshez csatolt működési specifikáció szerint elkészített, file-okból és mappákból álló szoftver

<br><br>
<b>Beüzemelési tehermentesítés</b>
<br><br>
A Beüzemelési tehermentesítés termék tartalma:<br>
- A Webfejlesztő vállalja, hogy a megrendelő megbízásából szerződést köt minden olyan szolgáltatóval, akiknek a szolgáltatására szükség van ahhoz, hogy a webes alkalmazás a specifikációban foglaltak szerint üzemelhessen.
<br>
Ez magában foglalja az alábbi típusú szolgáltatókat:<br>
a.: Domainregisztrátor: a webhely domain (tartománynév) alapján lesz elérhető. Ennek a szolgáltatását a világ bármely pontján domain-regisztráció tevékenységének végzésével feljogosított szervezetek végezhetik.<br>
b.: SSL tanusítványt kiállító szervezet: a Webfejlesztő csak úgy tudja garantálni az Európai Unió Általános adatvédelmi rendelettének (GDPR) megfelelést, amennyiben a webhely rendelkezik a titkosításhoz szükséges tanusítvánnyal. Így a tanusítvány nélkül nem vállal szerződéskötést sem, emiatt a Beüzemelési tehermentesítés termékbe az SSL tanusítvány megkérése is beletartozik.<br>
c.: Tárhely- és adatbázis-szolgáltató: a szoftver önmagában nem működik, a futtatásához szükség van egy kimondottan erre a célra készített számítógépre (szerver).

<br><br>
Tudnivalók a termékről:<br>
- A <b>Beüzemelési tehermentesítés</b> termék egy adott időszakban nyújtott szolgáltatás. A szolgáltatás kezdete: az a pillanat, amikor a Megrendelő és a Webfejlesztő között létrejött megállapodás értelmében a Webfejlesztő megkezdi a külső hatáskörbe tartozó szolgáltatások megrendelését. A szolgáltatás vége: az elkészült webes alkalmazás átadásától számított 30-adik nap.
<br>
- A Webfejlesztőnek joga van eldönteni, hogy elvállalja-e a <b>Beüzemelési tehermentesítés</b> szolgáltatás nyújtását.<br>
- <b>Beüzemelési tehermentesítés</b> termék kizárólag ElastiSite alapú webes alkalmazás megrendeléséhez kapcsolató.<br>
- A <b>Beüzemelési tehermentesítés</b> termék ára nem foglalja magában a különböző szolgáltatók felé fizetendő összegeket.<br>
- A beüzemeléshez szükséges szolgáltatások számláit a Megrendelő kell, hogy megfizesse. A szolgáltatók előfizetés keretében szolgáltatnak, tehát a szolgáltatás igénybevételének feltétele az első számla befizetése. 
- A szolgáltatók a saját előfizetési intervallumuk szerint számlát fognak kibocsátani a Megrendelő részére, ezeknek a számláknak a rendszeres és pontos rendezése a Webfejlesztő által készített termék felhasználásának alapfeltétele. <br>
- A Webfejlesztő nem vállalja, hogy felderíti a legolcsóbb szolgáltatókat. Olyan szolgáltatótól fog megrendelni, akinek a szolgáltatását ismeri és elégedett vele. A megrendelés előtt a Webfejlesztő egyeztet az árral kapcsolatban a Megrendelővel, és lehetőséget biztosít neki, hogy megbizonyosodjon attól, hogy a szolgáltatás ára nem tér el jelentősen (legfeljebb 30%) a piaci átlagártól. A megrendelőnek minden egyes szolgáltatás megrendelése előtt írásban kell rendelkeznie arról, hogy vállalja a szolgáltatás megrendelését, és az szolgáltatás megkezdéséhez szükséges árának megfizetését.<br>
- A <b>Beüzemelési tehermentesítés</b> szolgáltatás kizárólag a beüzemelésre szól, így a Beüzemelési hónapot követően a szolgáltató által a Megrendelő részére küldött bármilyen levél elolvasása, a probléma kezelése és a levél megválaszolása nem a Webfejlesztő feladata.

