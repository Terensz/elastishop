<b><i>Mi az ElastiSite?</i></b>
<br><br>
- Egy szoftver, amivel webes alkalmazásokat készítek. Rengeteg olyan beépített funkciót tartalmaz, ami miatt a kész webes alkalmazás sokkal megbízhatóbban teszi a dolgát, és nem igazán lehet feltörni. <br>
- Egy keretrendszer.
<br><br>
Az ElastiSite alapjai két év alatt készültek el. A kulcsfontosságú részeit ha kellett, 3-szor újraírtam, ha nem voltam tökéletesen elégedett a minőségével.
<br><br>
<b><i>Mit ad az ElastiSite?</i></b>
<br><br>
- Tartalmaz minden olyan védelmet, ami miatt idegenek nem tudják megszerezni az Ön személyes adatait<br>
- Standardizálttá teszi a webfejlesztést. Bár kevésbé izgalmas így a dolgom, mintha mindig hibákat kellene javítanom, cserébe viszont sokkal megbízhatóbb a késztermék, vagyis az Ön webes alkalmazása
<br><br>
<b><i>Az ElastiSite gyakorlatilag egy WordPress?</i></b>
<br><br>
Az ElastiSite és a WordPress hasonló feladatköröket ellátó szoftverek. Mindkettő különálló keretrendszer, és nem támogatják egymást.
