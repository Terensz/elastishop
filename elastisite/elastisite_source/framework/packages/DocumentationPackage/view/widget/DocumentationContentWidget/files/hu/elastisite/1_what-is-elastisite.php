
<b><i>Mi is ez az ElastiSite?</i></b><br><br>

<b>1.:</b> Egy <b>keretrendszer</b>. Tehát egy olyan program, ami előre elkészített megoldásokat, működési mechanizmusokat ad programozóknak, hogy azokat ne kelljen újra lefejleszteni.<br>
<br>
Ahogy pl. az autógyárak sem találják ki minden új típusnál, hogy milyen formájú legyen, milyen anyagból készüljön a jármű kereke, hanem csak újradizájnolják kicsit a meglevőt, ha az új modelhez erre szükség van.<br>
<br>
A keretrendszer meghatározza a vele készült webes alkalmazások alaptulajdonságait, működési jellemzőit. Pl. ezeket:<br>
- Milyen lesz a sebessége.<br>
- A webhely felhasználóinak az adatai mennyire lesznek biztonságban.<br>
- A vele készített program milyen gyorsan fog elkészülni és milyen hibaszázalékkal fog működni.<br>
- A vele készült webes alkalmazás mennyire működik korszerűen.<br>
<br>
<i>Következtetésképpen:</i><br>
<b><span style="color: #0c1a6f;">A webes alkalmazás minőségét sokkal inkább befolyásolja az, hogy milyen keretrendszerrel készítik, mint az, hogy ki készíti.</span> Tehát az alapok a legfontosabbak, mint egy lakóháznál.</b><br>
Természetesen a precíz egyedi fejlesztői munka is fontos, az is befolyásolja a jó működést.<br>
<br>

<b>2.:</b> Egy projekt, aminek az a célja, hogy biztonságos, gyors, megbízható és korszerű webhelyek kerüljenek az internetre a feltörhető, kattintásra lassan reagáló és elavultan működőek helyett, 
és hogy megcáfolja azt a hamis állítást, hogy a jó "weboldal"-hoz (webes alkalmazás) nem kell hozzáértő programozó, csak egy jó marketinges.<br>
<br>

<b><i>Egy ElastiSite webes alkalmazást a legjobb szívvel ajánlom Önnek, ha</i></b><br>
- észrevette, hogy ami ezen a webhelyen a kattintásai nyomán történik, az egy sokkal jobb és szebb működés, mint amit megszoktott, és ezt fontosnak is tartja<br>
- Ön az a típus, akinek a csúcsminőség kell, és azt gondolja, hogy a jól működő webes alkalmazás előrejelzi a termékei színvonalát<br>
- szeretné, ha az elkészült webes alkalmazásához később is hozzá tudna tenni egy webfejlesztő<br>
- fontosnak tartja, hogy könnyen be tudjon állítani szómagyarázatokat a cikkjeire<br>
- gyors és áttekinthető adminisztrációs felületet szeretne a tartalmaihoz<br>
- hisz abban, hogy a GDPR nem egy szükséges rossz, és fölösleges szabályozás, hanem tényleg szükség van a legjobb minőségű adatvédelemre.<br>
