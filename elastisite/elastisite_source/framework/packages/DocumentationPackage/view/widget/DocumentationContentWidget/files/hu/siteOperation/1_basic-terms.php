<h1>
    Weboldal, weblap
</h1>
<p>
    A böngészőbben az egy URL-címen (webcím) egyszerre betöltött szövegek és képek halmaza. Oldal lehet pl. a kezdőlap, a regisztrációs oldal, a bejelenkezési oldal, a webáruház terméklistája.
</p>

<h1>
    Domain
</h1>
<p>
    Angolul ennyit jelent: tulajdon, birtok. Egy karakterlánc (pl.: elastisite.com), ami áll egy kifejezésből, egy pontból és egy kötött két-három karakteres szórészletből, 
    ami vagy egy országot jelöl (pl.: hu), vagy a domain felhasználását (com: commercial - vagyis: kereskedelmi, org: organization - vagyis: szervezet). A domaint kell a böngésző címsorába beírni, és akkor töltődik be a webes tartalom. <br>
    A domain csak ez a két szó, és közöttük egy pont. Valamint az a jog, hogy valaki ezt a kifejezést használhatja. És még: iránymutatás a böngésző és a levelezőprogram számára, 
    hogy a domain tulajdonosának hol van a webszervere és a postafiókja. És ezen a három dolgon kívül semmi más.
</p>

<h1>
    Webhely, website
</h1>
<p>
    Az egy domain alatt található webolapok összessége. Amikor Önnek van egy webáruháza, akkor a teljes webhely magába foglalja a kezdőlapot, a regisztrációt, a terméklistákat, a fizetési felületet, a különféle ismertető oldalakat, 
    és még bármi más oldalt, ami az Ön webhelyének a domainje alatt található.<br>
    Sokan tévesen hívják weboldalnak, weblapnak a webhelyet.
</p>

<h1>
    Webshop
</h1>
<p>
    Egy olyan webhely, ami nem csak termékeket kínál, hanem azoknak az értékesítési folyamatát teljesen végig tudja vinni a látogatóval, aminek az lesz a végeredménye, hogy a vásárló a webshopban megrendelt terméket megkapja.<br>
    A termék lehet tárgy, élelmiszer, szolgáltatás, oktatás, bármi, ami értéket képvisel a vásárló számára.
</p>

<h1>
    Honlap
</h1>
<p>
    Ez a szó a magyar nyelvben egy félrefordítás eredménye. A 80-as évek végén fordította így le valaki az angol "homepage" szót, ami az angol nyelvben a webhely kezdőlapját (főoldal, index-oldal) jelenti a mai napig 
    (az adott domain első oldala, amikor a domain mögé nem írunk semmit). 
    A "honlap" szót ezzel szemben a teljes webhelyre szokták érteni, ami meg nem értések, félreértések forrása a megrendelő és a webfejlesztő között.
</p>

<h1>
    Landing oldal
</h1>
<p>
    A webhelynek egy marketing-célú oldala. Az, hogy milyen tartalom van rajta, a mindenkori marketing-trendeken múlik. A jelenlegi trend szerint egy kérdőívet kell tartalmaznia, ami adatot gyújt be a vásárlási szokásokról, valamint felkelti a vásárló figyelmét.<br>
    Fontos, hogy a landing oldal csak egy oldala egy webhelynek, vagyis aki odatéved, őt érdemes továbbkalauzolni a webshop felé, hogy a látogatása vásárlással végződjön.<br>
    Rendszeresen látok olyat, hogy egy fejlesztő landing oldalak elkészítését vállalja. Ez nem feltétlenül követi a marketing-trendeket (és itt a szakemberek által kikísérletezett és működő trendekről beszélek). A landing oldal csak része a nagy egésznek, nem pedig a nagy egész.
</p>

<h1>
    Keresőrobot
</h1>
<p>
    A kereső webhelyek (pl.: google.com) bizonyos időközönként végigjárják az interneten található webhelyeket, és ezeknek lehetőség szerint feltérképezik az összes oldalát. 
    Ezeket részben a menürendszereken, részben az oldalakon elhelyezett linkeken keresztül találják meg.
</p>