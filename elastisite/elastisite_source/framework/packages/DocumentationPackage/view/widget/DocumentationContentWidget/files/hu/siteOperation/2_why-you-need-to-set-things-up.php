<h1>
    Mi viszi a vásárlót a webáruházba?
</h1>
<p>
    Ahhoz, hogy az Ön webáruháza legyen pont az, amit a vásárló választ, ahhoz tudnunk kell, hogy milyen felületek viszik a látogatót a webáruházakba, és onnantól más dolgunk nincs, minthogy az adott felületek szempontjainak megfeleljünk.
</p>
<p>
    <b>Google keresés:</b>
Az emberek többsége amikor terméket vagy szolgáltatást keres, egyszerűen beírja annak a nevét a Google keresőbe, majd részben érzelmi benyomásoktól vezérelve az agya elkezdi elemezni a találatokat. 
A listából csak az első néhány webáruházat fogja megnézni, és ha ott nehézségekbe ütközik, akkor egyszerűen bezárja a böngésző-lapfület, és nézi a következőt. Akkor tér csak vissza egy korábbihoz, ha a következő néhánynál 
sem talál pont olyat és annyiért, mint ahogy azt eltervezte. 
</p>

<p>
    <b>Facebookos linkre (open graph) kattintás:</b>
Amikor valaki a Facebookon linkel egy weblapot, akkor a Facebook a linkből készít egy kiemelt címet, egy rövid leírást, és egy képet, esetleg egy képgyűjteményt. Az így elkészített linket a Facebook úgy nevezi: open graph. 
Vajon van jelentősége, hogy az open graph megfogja-e a felhasználót? 
Van, bizony. Jó hír, hogy Ön ezeket precízen, webcímekre bontva be tudja állítani a webhely-üzemeltetői felületen.
</p>

<h1>
    Mi kell ahhoz, hogy az Ön webáruháza az első helyekre kerüljön?
</h1>
<p>
A keresési helyezéseket alapvetően a Google által készített szoftver határozza meg, ami folyamatosan szkenneli a weblapokat, majd az így szerzett adatokat elemzi, és ez alapján rangsorolja azokat. 
Nekünk, aki az Ön webáruházát fejlesztettük (és fejlesztjük, korszerűsítjük folyamatosan) az egyik fő feladatunk az, hogy képben legyünk a keresőrobotokkal és a találati elemzésekkel kapcsolatban, 
és az Ön webáruházát folyamatosan megfeleltessük ezeknek. Azonban a mi munkánk csak az egyik felét fogja az Ön eredményeinek adni, arra is szükség van, hogy a webhelyén működő oldalakon mindig precízen 
legyenek beállítva a kulcsszavak, leírások, amit az Ön feladata lesz frissíteni.
</p>

<h1>
    Összefoglalva: miért kell beállítania dolgokat, vagyis: testreszabni a webhelyét?
</h1>
<p>
    - Azért, hogy a weblapjai (a webhelyének, webshopjának az oldalai) keresőkben (pl. Google) jobb helyezést érjenek el
</p>
<p>
    - Azért, hogy ha a Facebook-on Ön linkel egy saját weblapot, akkor a Facebook-os link (open graph) érthető, világos legyen, és bizalmat ébresszen a vásárlóban
</p>

