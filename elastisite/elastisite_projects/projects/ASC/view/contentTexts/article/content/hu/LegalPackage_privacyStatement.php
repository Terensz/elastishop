<h2>I.: A webhely üzemeltetője</h2>
A <b>[httpDomain]</b> webhelyet <b>[companyName]</b> üzemelteti (a továbbiakban: Webhely-üzemeltető).<br>
<br>

<h2>II.: A Webhely-üzemeltető nyilatkozata a személyes adatok közzétételéről</h2>
<b>1.:</b> A Webhely-üzemeltető kijelenti, hogy a felhasználó bármilyen személyes adatát (beleértve a felhasználónevét is) csakis olyan esetben teszi közzé, ha erre a felhasználó a Webhely-üzemeltetőnek felhatalmazást adott.
Ez a felhatalmazás minden esetben online történik.<br>
<br>

<h2>III.: A Webhely-üzemeltető nyilatkozata a személyes adatok tárolásáról és harmadik félnek átadásáról</h2>
<b>1.:</b> A Webhely-üzemeltető kijelenti, hogy a felhasználó személyes adatait csakis a szükséges esetben adja át harmadik személynek, ha a felhasználó olyan megrendelést végzett a webhelyen, 
aminek a teljesítéséhez szükséges harmadik fél bevonása.<br>
<b>2.:</b> A Webhely-üzemeltető megrendelési, számlázási és kiszállítási adatokat tárol, és ezek közül a szükségeseket megosztja 
a felhasználói megrendelés teljesítésében résztvevő szolgáltatókkal (pl. fizetési, szállítási szolgáltatók, átvevőpontok) és azokkal a megrendelés területén illetékes állami szervekkel, 
amelyek felé az adatok szolgáltatását törvény írja elő.<br>
<b>3.:</b> A Webhely-üzemeltető kijelenti, hogy ha a felhasználó visszavonja a saját adatainak kezelésére vonatkozó felhatalmazását, és azok az első pontban nem érintettek (pl. a megrendelés befejezetlen volt, 
és nem történt sem számlázás, sem fizetés, sem teljesítés) úgy a felhasználó írásos kérésére az összes személyes adatát 24 órán belül eltávolítja a webhelyhez kapcsolódó adatbázisból.
<br>
<br>

<h2>IV.: A Webhely-üzemeltető nyilatkozata a személyes adatok felhasználásáról </h2>
<b>1.:</b> A Webhely-üzemeltető a felhasználó személyes adatait az alábbi módokon használja fel: <br>
<b>&nbsp;&nbsp;a.:</b> Webáruházban végzett megrendelés teljesítéséhez tájékoztatás és a termékek kézbesítésének céljából.<br>
<b>&nbsp;&nbsp;b.:</b> A törvényben előírt kötelező adatszolgáltatás céljából. (Pl. számlakészítés esetén lejelentés a webáruház országában illetékes adóhivatalnak)<br>
<b>&nbsp;&nbsp;c.:</b> Hírlevél kiküldéséhez, amennyiben a felhasználó ehhez a tevékenységhez hozzájárult.<br>
<b>&nbsp;&nbsp;d.:</b> Megrendelési várólista létrehozásához.<br>
