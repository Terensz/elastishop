<p>
2001-ben jöhettem „haza” ismét, hogy a nulláról induljak („vissza a starthoz!”). Kezdődtek újra a végeláthatatlan műszakok, a munkával töltött pihenőnapok, 
a duplán fizetett túlórák, amelyek után, 2003-ra ismét sikerült annyi pénzt összegyűjtenem, hogy esélyem legyen háztulajdonossá válni. Ezúttal már nem is jött közbe semmi, 
beköltöztünk a házikónkba, egyidejűleg elindítottam a méhecskéimmel közös vállalkozásunkat: vándorméhész lettem.
</p>
<p>
A vándorméhészet lényege, hogy a gazda nem helyhez kötött kaptárokból indítja méheit, hanem folyamatosan figyelemmel kiséri a különböző növények porzási idejét, 
ennek megfelelően szállítja a kaptárokat a területre. Ez a módszer lehetővé teszi, hogy színtiszta, egynemű mézet nyerjen és kínáljon vevői számára.
</p>
<p>
Mindenki, akit tevékenysége a természethez köt, tudja, hogy ez a fajta munka a hivatás, a folytonos rácsodálkozás és a tanulás varázslatos elegye, egyszóval: szerelem. Hivatás, 
mert a munkám eredményével másokon is segíteni akarok és tudok; rácsodálkozás, mert a természet még ennyi idő után is naponta tartogat számomra meglepetéseket; tanulás, mert itt sosem 
lehet elégedetten hátradőlni, hiszen a holnap új kihívásokat hoz... és szerelem, mert az pont olyan egészséges és édes, mint a MÉZ.
</p>
