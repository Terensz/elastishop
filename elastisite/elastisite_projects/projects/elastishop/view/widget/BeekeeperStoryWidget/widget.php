<p>
<b>Békefi József</b> vagyok, méhész, és ez az én történetem. Viszontagságos, buktatókkal teli út vezetett odáig, hogy éppen 20 évvel ezelőtt valóra válthattam életem álmát: méhész lettem.
</p>
<p>
Az erdélyi Zilahon születtem 1977-ben, mindkét szülőm részéről méhész-dinasztia sarjaként. Egyik nagyapám vándorméhész volt, aki fáradhatatlanul kutatta az új lelőhelyeket, hogy aztán lovaskocsijával, sásból font kasokban szállítsa dolgozni apró „munkásait” a kiszemelt területre.
</p>
<p>
1995-ben, 18 évesen felkerekedtem, és Magyarországra jöttem szerencsét próbálni, ez azonban évekig valóban csak próbálkozás maradt.
</p>
<p>
Magyar állampolgárság és kezdő tőke hiányában nem volt sok választási lehetőségem; az építőiparban helyezkedtem el és dolgoztam keményen, hogy idővel a magam lábára állhassak. 
Bár mind a magyar, mind a román bürokrácia ellenem dolgozott, hiszen havonta kellett lepecsételtetnem az útlevelemet a határ túloldalán, a nehézségekkel dacolva 2000-re sikerült 
annyi pénzt összegyűjtenem, hogy lefoglalózhattam egy romos házikót. Az élet, vagy inkább a román hatóság azonban másként gondolta: egy pecsételtetési procedúra során közölték velem, 
hogy csak azután jöhetek vissza Magyarországra, hogy kitöltöttem az egy éves katonaidőmet; így hát a foglaló is elúszott és a reményeim is szertefoszlottak.
</p>
